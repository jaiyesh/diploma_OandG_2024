def addition(a,b):
    """
    it will do the addition of two numbers
    INPUT:
    a,b : two numbers

    RETURN:
    c = sum of a and b


    """

    c = a+b 
    return c


def substraction(a,b):
    """
    it will do the substraction of two numbers
    INPUT:
    a,b : two numbers

    RETURN:
    c = difference of a and b


    """

    c = a-b 
    return c

